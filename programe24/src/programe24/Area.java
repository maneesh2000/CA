package programe24;

public class Area {
	
	void circle(int r) {
		
		double area=3.14*r*r;
		System.out.println("Area of Circle is :"+area);
	}
	
void rectangle(int l,int b) {
		
		double area=l*b;
		System.out.println("Area of Circle is :"+area);
	}

void square(int a) {
	
	double area=a*a;
	System.out.println("Area of square is :"+area);
}


void triangle(int b,int h) {
	
	double area=(b*h)/2;
	System.out.println("Area of triangle is :"+area);
}

}
